# Anonymous Functions | Lambda Functions

# Lambda Functions -> Eg. - Multiplication 

f = lambda a : a*a

result = f(6)
print(result)


# Lambda Functions -> Eg. - Add
f = lambda a,b : a+b

result = f(6,5)
print(result)


# def Function
def square(a):
    return a*a

result = square(5)
print(result)
